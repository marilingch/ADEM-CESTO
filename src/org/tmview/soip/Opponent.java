package org.tmview.soip;

public class Opponent extends Person {

	private int OpponentIdentifier;
	private String OpponentType;
	
	
	public int getOpponentIdentifier() {
		return OpponentIdentifier;
	}
	public void setOpponentIdentifier(int opponentIdentifier) {
		OpponentIdentifier = opponentIdentifier;
	}
	public String getOpponentType() {
		return OpponentType;
	}
	public void setOpponentType(String opponentType) {
		OpponentType = opponentType;
	}
	
	
}
