package org.tmview.soip;

public class Nice {

	private String FileSeq;
	private String FileType;
	private int FileSeries;
	private int FileNumber;
	private int NiceClassNumber;
	private String NiceClassDesc;
	private String NiceClassStatus;
	private int NiceClassificationEdition;
	public String getFileSeq() {
		return FileSeq;
	}
	public void setFileSeq(String fileSeq) {
		FileSeq = fileSeq;
	}
	public String getFileType() {
		return FileType;
	}
	public void setFileType(String fileType) {
		FileType = fileType;
	}
	public int getFileSeries() {
		return FileSeries;
	}
	public void setFileSeries(int fileSeries) {
		FileSeries = fileSeries;
	}
	public int getFileNumber() {
		return FileNumber;
	}
	public void setFileNumber(int fileNumber) {
		FileNumber = fileNumber;
	}
	public int getNiceClassNumber() {
		return NiceClassNumber;
	}
	public void setNiceClassNumber(int niceClassNumber) {
		NiceClassNumber = niceClassNumber;
	}
	public String getNiceClassDesc() {
		return NiceClassDesc;
	}
	public void setNiceClassDesc(String niceClassDesc) {
		NiceClassDesc = niceClassDesc;
	}
	public String getNiceClassStatus() {
		return NiceClassStatus;
	}
	public void setNiceClassStatus(String niceClassStatus) {
		NiceClassStatus = niceClassStatus;
	}
	public int getNiceClassificationEdition() {
		return NiceClassificationEdition;
	}
	public void setNiceClassificationEdition(int niceClassificationEdition) {
		NiceClassificationEdition = niceClassificationEdition;
	}
	
	
}
